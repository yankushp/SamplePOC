package com.auto.test.utility;

public class ReportConfig {

    private String environment;
    private String environmentType;
    private String developedBy;
    private String yankushPrasad;
    private String extentConfigFile;
    private String reportName;

    public String getEnvironment() {
        return environment;
    }
    public void setEnvironment(String environment) {
        this.environment = environment;
    }
    public String getEnvironmentType() {
        return environmentType;
    }
    public void setEnvironmentType(String environmentType) {
        this.environmentType = environmentType;
    }
    public String getDevelopedBy() {
        return developedBy;
    }
    public void setDevelopedBy(String developedBy) {
        this.developedBy = developedBy;
    }
    public String getYankushPrasad() {
        return yankushPrasad;
    }
    public void setYankushPrasad(String yankushPrasad) {
        this.yankushPrasad = yankushPrasad;
    }
    public String getExtentConfigFile() {
        return extentConfigFile;
    }
    public void setExtentConfigFile(String extentConfigFile) {
        this.extentConfigFile = extentConfigFile;
    }
    public String getReportName() {
        return reportName;
    }
    public void setReportName(String reportName) {
        this.reportName = reportName;
    }
}
